lib.app = function(rootID,baseUI) {
	this.ui = {};
	this.setRoot(rootID);
	this.setBaseUI(baseUI);
};

// -------------------------------------------------------------------------------------------------
//
// -------------------------------------------------------------------------------------------------
lib.app.prototype.initialize = function() {
	with(this.ui) { root.appendChild(base.dom); }
};

// -------------------------------------------------------------------------------------------------
//
// -------------------------------------------------------------------------------------------------
lib.app.prototype.setRoot = function(rootID) {
	if(rootID) { this.ui.root = document.getElementById(rootID); }
};

// -------------------------------------------------------------------------------------------------
//
// -------------------------------------------------------------------------------------------------
lib.app.prototype.setBaseUI = function(uiObj) {
	if(uiObj) { this.ui.base = uiObj; }
};