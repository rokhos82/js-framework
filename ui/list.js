////////////////////////////////////////////////////////////////////////////////////////////////////
// ui.list
////////////////////////////////////////////////////////////////////////////////////////////////////
ui.list = function(ordered) {
	if(ordered)
		this.dom = document.createElement("ol")
	else
		this.dom = document.createElement("ul");
	
	this.parent = null;
	this.children = new Array();
};

// -------------------------------------------------------------------------------------------------
//
// -------------------------------------------------------------------------------------------------
ui.list.prototype.setParent = function(parent) {
	this.parent = parent;
};

// -------------------------------------------------------------------------------------------------
//
// -------------------------------------------------------------------------------------------------
ui.list.prototype.appendChild = function(child) {
	child.setParent(this);
	this.dom.appendChild(child.dom);
	this.children.push(child);
};

// -------------------------------------------------------------------------------------------------
//
// -------------------------------------------------------------------------------------------------
ui.list.prototype.refreshView = function() {
};

// -------------------------------------------------------------------------------------------------
//
// -------------------------------------------------------------------------------------------------
ui.list.prototype.removeChildren = function() {
	for(var c in this.children) {
		this.children[c].removeChildren();
		this.dom.removeChild(this.children[c].dom);
		delete this.children[c];
	}
};

// -------------------------------------------------------------------------------------------------
//
// -------------------------------------------------------------------------------------------------
ui.list.prototype.addItem = function(text) {
	var li = new ui.listItem(text);
	this.appendChild(li);
	return li;
};

// -------------------------------------------------------------------------------------------------
//
// -------------------------------------------------------------------------------------------------
ui.list.prototype.addAnchorItem = function(text,name,href) {
	var ai = new ui.anchorItem(text,name,href);
	this.appendChild(ai);
	return ai;
};

////////////////////////////////////////////////////////////////////////////////////////////////////
// ui.listItem
////////////////////////////////////////////////////////////////////////////////////////////////////
ui.listItem = function(text) {
	this.dom = document.createElement("li");
	this.dom.innerHTML = text;
	this.parent = null;
};

// -------------------------------------------------------------------------------------------------
//
// -------------------------------------------------------------------------------------------------
ui.listItem.prototype.setParent = function(parent) {
	this.parent = parent;
};

// -------------------------------------------------------------------------------------------------
//
// -------------------------------------------------------------------------------------------------
ui.listItem.prototype.refreshView = function() {
};

// -------------------------------------------------------------------------------------------------
//
// -------------------------------------------------------------------------------------------------
ui.listItem.prototype.removeChildren = function() {
};

////////////////////////////////////////////////////////////////////////////////////////////////////
// ui.listItem
////////////////////////////////////////////////////////////////////////////////////////////////////
ui.anchorItem = function(text,name,href) {
	this.dom = document.createElement("li");
	this.anchor = document.createElement("a");
	this.anchor.innerHTML = text;
	this.anchor.name = name;
	this.anchor.href = href;
	this.dom.appendChild(this.anchor);
	this.parent = null;
};

// -------------------------------------------------------------------------------------------------
//
// -------------------------------------------------------------------------------------------------
ui.anchorItem.prototype.setParent = function(parent) {
	this.parent = parent;
};

// -------------------------------------------------------------------------------------------------
//
// -------------------------------------------------------------------------------------------------
ui.anchorItem.prototype.refreshView = function() {
};

// -------------------------------------------------------------------------------------------------
//
// -------------------------------------------------------------------------------------------------
ui.anchorItem.prototype.removeChildren = function() {
};